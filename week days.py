def назва_дня_тижня(номер_дня):
    дні_тижня = {
        1: "Понеділок",
        2: "Вівторок",
        3: "Середа",
        4: "Четвер",
        5: "П'ятниця",
        6: "Субота",
        7: "Неділя"
    }
    return дні_тижня.get(номер_дня, "Невірний номер дня")
номер_дня = int(input("Введіть номер дня тижня (1-7): "))

назва = назва_дня_тижня(номер_дня)
print("День тижня:", назва)